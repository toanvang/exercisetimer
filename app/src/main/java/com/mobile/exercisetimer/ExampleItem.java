package com.mobile.exercisetimer;

public class ExampleItem {
    private String exercise;
    private String rest;

    public ExampleItem(String exercise, String rest) {
        this.exercise = exercise;
        this.rest = rest;
    }

    public String getExercise() {
        return exercise;
    }

    public String getRest() {
        return rest;
    }
}
